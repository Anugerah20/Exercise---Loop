// Membuat variabel nilai default-nya itu 0
let nilai = 0;
// nilai, Lakukan inisialisasi variabel nilai dengan angka 0
// nilai <= 10, Buat kondisi sampai angka yang di isi terpenuhi
// nilai++, Increment berfungsi untuk menambahkan 1 angka setiap pengulangan
for(nilai; nilai <= 10; nilai++) {
     console.log(nilai); // Melihat hasil di inspect element bagian console
}
